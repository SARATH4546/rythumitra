    crop_hint = sys.argv[2] if len(sys.argv) > 2 else ""

    if not os.path.exists(img_path):
        print(json.dumps({"success": False, "error": f"File not found: {img_path}"}))
        sys.exit(1)

    result = predict(img_path, crop_hint)
