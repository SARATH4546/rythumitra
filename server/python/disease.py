# ─── Dynamic class labels (generated by merge_datasets.py) ───────────────────
# If class_labels.json exists (after running merge_datasets.py), load it.
# Otherwise fall back to built-in PlantVillage 38-class list.
def load_class_labels():
    if os.path.exists(LABELS_PATH):
        with open(LABELS_PATH, encoding="utf-8") as f:
            data = json.load(f)
        # Sort by index
        classes = [None] * len(data)
        for cls, info in data.items():
            idx = info["index"]
            if idx < len(classes):
                classes[idx] = cls
        classes = [c for c in classes if c]
        print(f"[Disease] Loaded {len(classes)} classes from {LABELS_PATH}", file=sys.stderr)
        return classes, data
    return None, None

_dynamic_classes, _dynamic_labels = load_class_labels()


# ─── Parse class label ────────────────────────────────────────────────────────
def parse_class(cls):
    parts      = cls.split("___")
    plant      = parts[0].replace("_", " ") if parts else "Unknown"
    disease    = parts[1] if len(parts) > 1 else "unknown"
    is_healthy = "healthy" in disease.lower()
    return plant, disease, is_healthy


def get_telugu_info(disease_key):
    for k, v in TELUGU_INFO.items():
        if k.lower().replace("_","") in disease_key.lower().replace("_","").replace(" ",""):
            return v
    return {"te": disease_key.replace("_", " "), "sev": "moderate"}


